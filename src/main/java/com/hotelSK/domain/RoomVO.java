package com.hotelSK.domain;

public class RoomVO {
	private int room_id;
	private int room_no;
	private String room_type;
	private String room_bedType;
	private int room_size;
	private String room_view;
	private int room_fare;
	private String hotel_name;
	
	public int getRoom_id() {
		return room_id;
	}
	public void setRoom_id(int room_id) {
		this.room_id = room_id;
	}
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	public String getRoom_type() {
		return room_type;
	}
	public void setRoom_type(String room_type) {
		this.room_type = room_type;
	}
	public String getRoom_bedType() {
		return room_bedType;
	}
	public void setRoom_bedType(String room_bedType) {
		this.room_bedType = room_bedType;
	}
	public int getRoom_size() {
		return room_size;
	}
	public void setRoom_size(int room_size) {
		this.room_size = room_size;
	}
	public String getRoom_view() {
		return room_view;
	}
	public void setRoom_view(String room_view) {
		this.room_view = room_view;
	}
	public int getRoom_fare() {
		return room_fare;
	}
	public void setRoom_fare(int room_fare) {
		this.room_fare = room_fare;
	}
	public String getHotel_name() {
		return hotel_name;
	}
	public void setHotel_name(String hotel_name) {
		this.hotel_name = hotel_name;
	}
	

}
